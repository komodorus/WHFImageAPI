    {

        $this->validate($request, [
            'image' => 'required|image|mimes:jpeg,png,jpg,gif,svg|max:2048',
        ]);

        $path = $request->image->store('uploads', 'public');

        $uploadedImage = Image::create([
            'path' => $path
        ]);

        return response()->json([
            'uploaded_image_url' => asset($path)
        ]);

